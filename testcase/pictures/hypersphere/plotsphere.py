from mpl_toolkits.mplot3d import Axes3D
import matplotlib.pyplot as plt
import numpy as np
from itertools import product, combinations


fig = plt.figure()
ax = fig.gca(projection='3d')
ax.set_aspect("equal")


# draw sphere
u, v = np.mgrid[0:2*np.pi:20j, 0:np.pi:20j]
x = np.cos(u)*np.sin(v)
y = np.sin(u)*np.sin(v)
z = np.cos(v)
ax.plot_surface(x, y, z, color="r", alpha=0.3)
ax.plot_wireframe(x, y, z, color="r", alpha=0.5)

u, v = np.mgrid[0:2*np.pi:20j, 0:np.pi:10j]
x = np.cos(u)*np.sin(v)
y = np.sin(u)*np.sin(v)
z = np.cos(v)
ax.plot_surface(2*x, 2*y, 2*z, color="b", alpha=0.1)
ax.plot_wireframe(2*x, 2*y, 2*z, color="b", alpha=0.5)


# draw a point
ax.scatter([0], [0], [0], color="g", s=100)

plt.title("Surface of the hypersphere")
plt.savefig("samplingfromhypersphere.png")
